    private void startOpton() throws SQLException, ClassNotFoundException{
    try {
        // Retrieve the customer data
        ResultSet result = CrudUtil.execute("SELECT * FROM CUSTOMER WHERE CUSTOMER_ID=?", FirstInterface.Username);

        if (result.next()) {
            String CUSid = result.getString("CUSTOMER_ID");
            cusid.setText(CUSid);

            String fname = result.getString("CUSTOMER_FIRST_NAME");
            String lname = result.getString("CUSTOMER_LAST_NAME");
            cusname.setText(fname + " " + lname);
        } else {
            // Handle the case where no data is found for the given CUSTOMER_ID
            JOptionPane.showMessageDialog(null, "Customer not found.");
        }

        // Retrieve the last record from CUSTOMER_FEEDBACK table
        ResultSet feedbackResult = CrudUtil.execute("SELECT * FROM CUSTOMER_FEEDBACK");

        if (feedbackResult.next()) {
            String lastFeedbackId = feedbackResult.getString("CUSTOMER_FEEDBACK_ID");

            // Extract the numeric part of the last feedback ID
            String numericPart = lastFeedbackId.replaceAll("[^0-9]", "");

            
            int nextNumericPart = Integer.parseInt(numericPart) ;
            String nextFeedbackId = "feed" + String.format("%03d", nextNumericPart);

            FEEDBACKIDL.setText(nextFeedbackId);
        } else {
            
            JOptionPane.showMessageDialog(null, "No feedback data found.");
        }

    } catch (SQLException | ClassNotFoundException ex) {
        
        JOptionPane.showMessageDialog(null, "Error accessing the database.");
    }



        

    }
