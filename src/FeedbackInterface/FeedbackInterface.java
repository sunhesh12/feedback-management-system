    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        String rate_type = null;
        String type = null;
        switch (x) {
            case 1 -> {
                type = "very bad";
            }
            case 2 -> {
                type = "bad";
            }
            case 3 -> {
                type = "average";
            }
            case 4 -> {
                type = "good";
            }
            case 5 -> {
                type = "excellent";
            }
            default -> {
                JOptionPane.showMessageDialog(null, "Please Give a reaction by clicking the face.");
            }
        }

        if (ser.isSelected()) {
            rate_type = "Service";
        } else if (pro.isSelected()) {
            rate_type = "Product";
        } else if (sernpro.isSelected()) {
            rate_type = "Service and Product";
        }
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date date = new Date();
        String Date = formatter.format(date);

        String feedid = FEEDBACKIDL.getText();
        String Rate = type;
        String Comment = CommentTxt.getText();
        String type1 = rate_type;
        String Customer = cusid.getText();

        try {
            CrudUtil.execute("INSERT INTO CUSTOMER_FEEDBACK (CUSTOMER_FEEDBACK_ID, DATE, CUSTOMER_RATING, COMMENT, TYPE, CUSTOMER_ID) VALUES (?, ?, ?, ?, ?, ?)",
                    feedid, Date, Rate, Comment, type1, Customer);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(SingUpInterface.class.getName()).log(Level.SEVERE, null, ex);
            // Handle the exception appropriately, e.g., show an error message.
            JOptionPane.showMessageDialog(null, "Error inserting feedback data.");
        }
   
        JOptionPane.showMessageDialog(null, "Thank you for your feedback sir..");    
    }//GEN-LAST:event_jButton3MouseClicked

